/*
  # Create tables for GreyEd Teachers platform

  1. New Tables
    - `classes`: For teacher class management
    - `lesson_plans`: Stores AI-generated lesson plans
    - `resources`: Teaching materials uploaded by teachers
    - `assessments`: Quiz/test configurations
    - `assessment_items`: Individual questions in assessments
    - `grades`: Student assessment results
    - `family_updates`: Weekly parent/guardian communication

  2. Security
    - Enable RLS on all tables
    - Add policies to ensure teachers can only access their own data

  3. Schema Details
    - All tables reference the teacher's user ID
    - Proper relationships established between tables
    - Support for AI-generated content
*/

-- Create classes table (core table for teacher functionality)
CREATE TABLE IF NOT EXISTS classes (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  teacher_id UUID REFERENCES auth.users ON DELETE CASCADE,
  name TEXT NOT NULL,
  subject TEXT NOT NULL,
  grade TEXT NOT NULL,
  student_count INTEGER DEFAULT 0,
  description TEXT,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- Enable RLS on classes
ALTER TABLE classes ENABLE ROW LEVEL SECURITY;

-- Create policy for teachers to manage their own classes
CREATE POLICY "Teachers can manage their own classes"
  ON classes
  FOR ALL
  USING (teacher_id = auth.uid())
  WITH CHECK (teacher_id = auth.uid());

-- Create lesson_plans table
CREATE TABLE IF NOT EXISTS lesson_plans (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  class_id UUID REFERENCES classes(id) ON DELETE CASCADE,
  date DATE NOT NULL,
  topic TEXT NOT NULL,
  md_path TEXT,
  meta JSONB,
  status TEXT CHECK (status IN ('draft', 'ready', 'taught')),
  created_at TIMESTAMPTZ DEFAULT now()
);

-- Enable RLS on lesson_plans
ALTER TABLE lesson_plans ENABLE ROW LEVEL SECURITY;

-- Create policy for lesson plans
CREATE POLICY "Teachers can manage lesson plans for their classes"
  ON lesson_plans
  FOR ALL
  USING (
    class_id IN (
      SELECT id FROM classes WHERE teacher_id = auth.uid()
    )
  )
  WITH CHECK (
    class_id IN (
      SELECT id FROM classes WHERE teacher_id = auth.uid()
    )
  );

