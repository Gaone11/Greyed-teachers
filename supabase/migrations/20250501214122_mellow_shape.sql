-- Create assessment_items table
CREATE TABLE IF NOT EXISTS assessment_items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  assessment_id BIGINT REFERENCES assessments(id) ON DELETE CASCADE,
  question TEXT NOT NULL,
  correct_answer TEXT NOT NULL,
  metadata JSONB
);

-- Enable RLS on assessment_items
ALTER TABLE assessment_items ENABLE ROW LEVEL SECURITY;

-- Create policy for assessment items
CREATE POLICY "Teachers can manage assessment items for their assessments"
  ON assessment_items
  FOR ALL
  USING (
    assessment_id IN (
      SELECT a.id FROM assessments a
      JOIN classes c ON a.class_id = c.id
      WHERE c.teacher_id = auth.uid()
    )
  )
  WITH CHECK (
    assessment_id IN (
      SELECT a.id FROM assessments a
      JOIN classes c ON a.class_id = c.id
      WHERE c.teacher_id = auth.uid()
    )
  );

-- Create grades table
CREATE TABLE IF NOT EXISTS grades (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  assessment_item_id BIGINT REFERENCES assessment_items(id) ON DELETE CASCADE,
  student_id TEXT NOT NULL,
  student_name TEXT,
  score NUMERIC,
  response TEXT,
  graded_at TIMESTAMPTZ DEFAULT now()
);

-- Enable RLS on grades
ALTER TABLE grades ENABLE ROW LEVEL SECURITY;

-- Create policy for grades
CREATE POLICY "Teachers can manage grades for their assessments"
  ON grades
  FOR ALL
  USING (
    assessment_item_id IN (
      SELECT ai.id FROM assessment_items ai
      JOIN assessments a ON ai.assessment_id = a.id
      JOIN classes c ON a.class_id = c.id
      WHERE c.teacher_id = auth.uid()
    )
  )
  WITH CHECK (
    assessment_item_id IN (
      SELECT ai.id FROM assessment_items ai
      JOIN assessments a ON ai.assessment_id = a.id
      JOIN classes c ON a.class_id = c.id
      WHERE c.teacher_id = auth.uid()
    )
  );

